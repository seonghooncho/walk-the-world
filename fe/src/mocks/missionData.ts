export type ProofType = "photo" | "text" | "session" | "screenshot" | "social";
export type MissionType = ProofType;
export type MissionStatus = "locked" | "available" | "completed";

export interface Mission {
  id: string;
  cityId: string;
  type: MissionType;
  proofType: ProofType;
  title: string;
  description: string;
  image: string;
  /** API 호환용 값. 새 제품 경험은 티켓/스탬프 조건을 우선 사용한다. */
  stepsRequired: number;
  ticketsRequired: number;
  stampsRequired: number;
  emoji: string;
  reward?: string;
  stampReward: string;
  ticketReward: number;
  sessionHint: string;
  verificationLabel: string;
  proofExamples: string[];
  /** 런타임에서 계산됨 - 원본 데이터에선 무시 */
  status: MissionStatus;
  aiComposite?: boolean;
  aiPrompt?: string;
}

const SESSION_STEP_PROXY = 75_000;
const STAMP_STEP_PROXY = 150_000;

export function estimateTravelTickets(totalSteps: number): number {
  return Math.max(3, Math.floor(totalSteps / SESSION_STEP_PROXY));
}

export function estimateStampCount(totalSteps: number, completedMissionCount = 0): number {
  return Math.max(completedMissionCount, Math.min(12, Math.floor(totalSteps / STAMP_STEP_PROXY)));
}

export function computeMissionStatus(
  mission: Mission,
  userTotalSteps: number,
  isCityUnlocked: boolean,
  completedMissions: Set<string>,
): MissionStatus {
  if (completedMissions.has(mission.id)) return "completed";
  if (!isCityUnlocked) return "locked";

  const tickets = estimateTravelTickets(userTotalSteps);
  const stamps = estimateStampCount(userTotalSteps, completedMissions.size);

  if (tickets >= mission.ticketsRequired && stamps >= mission.stampsRequired) {
    return "available";
  }
  return "locked";
}

export function computeCityMissions(
  cityId: string,
  userTotalSteps: number,
  isCityUnlocked: boolean,
  completedMissions: Set<string>,
): Mission[] {
  const missions = cityMissions[cityId] || [];
  return missions.map((mission) => ({
    ...mission,
    status: computeMissionStatus(mission, userTotalSteps, isCityUnlocked, completedMissions),
  }));
}

const mission = (
  data: Omit<Mission, "status" | "aiComposite" | "aiPrompt"> & Partial<Pick<Mission, "aiComposite" | "aiPrompt">>,
): Mission => ({
  ...data,
  status: "available",
});

