export function getMissionProgress(cityId: string): { completed: number; total: number } {
  const missions = cityMissions[cityId] || [];
  const completed = missions.filter((currentMission) => currentMission.status === "completed").length;
  return { completed, total: missions.length };
}

export function getCityMissionStats(cityId: string) {
  const missions = cityMissions[cityId] || [];
  return {
    total: missions.length,
    completed: missions.filter((currentMission) => currentMission.status === "completed").length,
    available: missions.filter((currentMission) => currentMission.status === "available").length,
    locked: missions.filter((currentMission) => currentMission.status === "locked").length,
  };
}
